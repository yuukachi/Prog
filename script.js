function showContact() {
    const municipality = document.getElementById("municipality").value;
    const department = document.getElementById("department").value;
    const contactInfoDiv = document.getElementById("contactInfo");
    const departmentLogo = document.getElementById("departmentLogo");

    if (municipality && department) {
        const contactData = contacts[municipality]?.[department];
        
        if (contactData) {
            contactInfoDiv.innerHTML = `<p><strong>${contactData.number}</strong></p>`;
            departmentLogo.src = contactData.logo;
            departmentLogo.style.display = "block";
        } else {
            contactInfoDiv.innerHTML = "<p>Contact not available.</p>";
            departmentLogo.style.display = "none";
        }
    } else {
        contactInfoDiv.innerHTML = "<p>Please select both a municipality and department.</p>";
        departmentLogo.style.display = "none";
    }
    
}
